public class BooleanosPrimitivos {
    public static void main(String[] args) {

        boolean DatoLogico = true; // o true
        System.out.println("DatoLogico = " + DatoLogico);

        double Double = 65475.45e-7;
        System.out.println("Double = " + Double);
        
        float Float = 2.4544e8F;
        System.out.println("Float = " + Float);

        DatoLogico = Double > Float; //True
        System.out.println("DatoLogico = " + Float);

        //Operacion anidada = (3-2 ==1);
        boolean Operacion = 3-2 ==1;
        System.out.println("Operacion = " + Operacion);

    }
}
